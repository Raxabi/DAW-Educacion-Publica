package JuegoUrna;

public class Urna {

    private int bolasBlancas;

    private int bolasNegras;

    public void setBolasBlancas(int numeroBolas) {
        this.bolasBlancas = numeroBolas;
    }

    public void setBolasNegras(int numeroBolas) {
        this.bolasNegras = numeroBolas;
    }

    public int getBolasBlancas() {
        return this.bolasBlancas;
    }

    public int getBolasNegras() {
        return this.bolasNegras;
    }
}
